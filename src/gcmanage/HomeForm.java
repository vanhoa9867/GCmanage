    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        // TODO add your handling code here:
        String key = txtSearch.getText();
        String SQL = "select * from HANGHOA where "
                + "MaHH like '%" + key + "%' or TenHH like N'%" + key + "%'"
                + " or GhiChu like N'%" + key + "%'"
                + " or DonGia like '%" + key + "%' or NhomHH like '%" + key + "%'";
        ResultSet rs = null;
        try {
            rs = new DataConnection().ExcuteQuery(SQL);
        } catch (Exception ex) {
            Logger.getLogger(HomeForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        Object[] obj = new Object[]{"Mã", "Nhóm", "Tên", "Ghi chú", "Hình ảnh", "Đơn giá"};
        DefaultTableModel tableModel = new DefaultTableModel(obj, 0);
        jTableHangHoa.setModel(tableModel);
        try {
            while (rs.next()) {
                Object[] item = new Object[10];
                item[0] = jTableHangHoa.getRowCount() + 1;
                item[1] = rs.getInt("MaHH");
                item[2] = rs.getString("NhomHH");
                item[3] = rs.getString("TenHH");
                item[4] = rs.getString("GhiChu");
                item[5] = rs.getString("HinhAnh");
                item[6] = rs.getInt("DonGia");
                tableModel.addRow(item);
            }
        } catch (Exception e) {
            System.out.println("Không tìm thấy!");
        }
    }//GEN-LAST:event_txtSearchKeyReleased

    private void jTableHangHoaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableHangHoaMouseClicked
        // TODO add your handling code here:
        try {
            txtNotification.setText("");
            DefaultTableModel model = (DefaultTableModel) jTableHangHoa.getModel();
            DefaultTableModel DonHang = (DefaultTableModel) jTableDonHang.getModel();
            Object[] obj = new Object[]{"STT", "Mã", "Nhóm", "Tên", "Ghi chú", "Hình ảnh", "Đơn giá", "Số lượng"};
            int row = jTableHangHoa.getSelectedRow();
            int rowcheck = jTableDonHang.getRowCount();
            int sl = 1;

            String mahh = model.getValueAt(row, 0).toString();
            String nhomhh = model.getValueAt(row, 1).toString();
            String tenhh = model.getValueAt(row, 2).toString();
            String ghichu = model.getValueAt(row, 3).toString();
            String hinhanh = model.getValueAt(row, 4).toString();
            String dongia = model.getValueAt(row, 5).toString();
            HangHoa temp = new HangHoa(Integer.parseInt(mahh), nhomhh, tenhh, ghichu, hinhanh, Integer.parseInt(dongia));

            int sumPrice = Integer.parseInt(dongia);
            int totalPrice = Integer.parseInt(dongia);
            int totalQuantity = sl;
            //check
            if (rowcheck == 0) {
                DonHang.addRow(new Object[]{temp.MaHH, temp.NhomHH, temp.TenHH, temp.GhiChu, temp.DonGia, sl, sumPrice});
            } else {
                boolean init = false;
                for (int i = 0; i < rowcheck; i++) {
                    String tempPrice = DonHang.getValueAt(i, 6).toString();
                    String tempQuantity = DonHang.getValueAt(i, 5).toString();
                    totalPrice += Integer.parseInt(tempPrice);
                    totalQuantity += Integer.parseInt(tempQuantity);
                    String check = DonHang.getValueAt(i, 0).toString();
                    if (Integer.parseInt(mahh) == Integer.parseInt(check)) {
                        init = true;
                        String a = DonHang.getValueAt(i, 5).toString();
                        sl = Integer.parseInt(a) + 1;
                        sumPrice = sl * Integer.parseInt(dongia);
                        DonHang.setValueAt(sl, i, 5);
                        DonHang.setValueAt(sumPrice, i, 6);
                    }
                }
                if (init == false) {
                    DonHang.addRow(new Object[]{temp.MaHH, temp.NhomHH, temp.TenHH, temp.GhiChu, temp.DonGia, sl, sumPrice});
                }
            }
            txtTotalQuantity.setText(String.valueOf(totalQuantity));
            txtTotalPrice.setText(String.valueOf(totalPrice));

            int ck = Integer.parseInt(txtChietKhau.getText());
            int vat = Integer.parseInt(txtVAT.getText());

            float fn = ThanhTien(totalPrice, vat, ck);
            txtTotal.setText(String.valueOf(fn));
            int cost = Integer.parseInt(txtCost.getText());
            float refund = cost - fn;
            txtCostRefund.setText(String.valueOf(refund));

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jTableHangHoaMouseClicked

