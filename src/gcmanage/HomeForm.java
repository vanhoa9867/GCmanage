    private void refresh(){
        int ck = Integer.parseInt(txtChietKhau.getText());
        int vat = Integer.parseInt(txtVAT.getText());
        float total = Float.parseFloat(txtTotalPrice.getText());
        
        float fn = ThanhTien(total, vat, ck);
        txtTotal.setText(String.valueOf(fn));
        int cost = Integer.parseInt(txtCost.getText());
        float refund = cost - fn;
        txtCostRefund.setText(String.valueOf(refund));
    }
    
    private float ThanhTien(float totalPrice, int VAT, int ChietKhau) {
        float temp1;
        float temp2;
        if (VAT == 0) {
            temp1 = 0;
        } else {
            temp1 = totalPrice * ChietKhau / 100;
        }

        if (ChietKhau == 0) {
            temp2 = 0;
        } else {
            temp2 = totalPrice * VAT / 100;
        }
        return totalPrice - temp1 + temp2;
    }

